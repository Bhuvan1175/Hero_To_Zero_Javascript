// While Loop
let i = 0;
while (i <= 10) {
  if (i % 2 == 0) {
    console.log(`${i} is even Number`);
  }
  i++;
}
// Array

let myArray = ["flash", "batman", "superman"];
let arr = 0;
while (arr < myArray.length) {
  console.log(myArray[arr]);
  arr++;
}

// Do While Loop
let score = 1;
do {
  console.log(`Score is ${score}`);
  score++;
} while (score <= 10);
