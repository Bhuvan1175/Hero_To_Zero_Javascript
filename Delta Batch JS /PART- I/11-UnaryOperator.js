// unary operator 
// unary operator is an operator that takes only one operand 

// pre-increment (change, then used)
let age=21;
console.log(++age);
console.log(age);
// post-increment (used, then change)
let price=99;
console.log(price++);
console.log(price);
// pre-decrement
let wafers=5;
console.log(--wafers);
console.log(wafers);
// post-decrement
let dressPrice=299;
console.log(dressPrice--);
console.log(dressPrice);

