//Let keyword
// Syntax of declaring variable
// let variableName = value;

let age=21;
age=18; // can be reassigned
console.log(age+1);


// Const Keyword
// value of constants can't be changed with re-assignment & they cant be redeclare.
// syntax of declaring variable using const keyword
// const variableName = value;

const pi=3.14
// const pi=9; we cant redeclare and not reassigned.
console.log(pi);


